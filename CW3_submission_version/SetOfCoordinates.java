
/**
 * Class that can store a pair of Integers standing for coordinates.
 *
 * @author Miriam Czech, K21099181 and Shun Sheng Lee, K21081997
 * @version 2022.02.28
 */
public class SetOfCoordinates
{
    // instance variables
    public Integer x;
    public Integer y;

    /**
     * Constructor for objects of class SetOfCoordinates
     */
    public SetOfCoordinates()
    {
    }
    
    /**
     * Constructor for objects of class SetOfCoordinates
     */
    public SetOfCoordinates(Integer x, Integer y)
    {
        this.x = x;
        this.y = y;
    }
}
